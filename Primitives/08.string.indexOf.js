//indexOf method returns the index of the first occurrence of a specified value in a string.
//If the specified value is not found, it returns -1.

let str = "Welcome to JavaScript";
console.log(str.indexOf("JavaScript")); // 11
console.log(str.indexOf("Python")); // -1
console.log(str.indexOf("script")); // -1
console.log(str.indexOf("Script")); // 15
